# Görev 1: Başlangıç değeri 10 olan bir sayi değişkeni oluşturun. 
# Sırasıyla aşağıdaki işlemleri atama operatörleriyle (+=, -=, *=, /=) uygulayın:

sayi = 10
sayi += 5
sayi -= 3
sayi *= 2
sayi /= 4
print("Sonuç:", sayi)

# Görev 2: metin = "Python" değişkeni oluşturun. += operatörüyle bu metnin sonuna "  Programlama" ekleyip yeni metni yazdırın.

metin = "Python"
metin += " Programlama"
print("Yeni metin:", metin)
